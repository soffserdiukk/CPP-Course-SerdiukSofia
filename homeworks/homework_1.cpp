#include <stdio.h> // Бібліотека для printf 

int main() {
    // ==========================================================
    // ЗАВДАННЯ 17: Піднесення до 8-го степеня з точним форматом
    // ==========================================================
    double x17, res17;

    printf("\n=== TASK 17 RESULTS ===\n");
    printf("Введіть число від 0 до 10000: ");
    scanf("%lf", &x17);

    // Обчислюємо 8-й степінь через послідовне квадратування (3 операції)
    res17 = x17 * x17;      // x^2
    res17 = res17 * res17;  // x^4
    res17 = res17 * res17;  // x^8

    // Форматування: %25.4f
    // 25 - загальна ширина поля (щоб вкластися у "до 20 знаків до коми" + крапка + 4 після)
    // .4 - рівно 4 знаки після коми
    printf("Результат x^8: %25.4f\n", res17);

    // ==========================================================
    // ЗАВДАННЯ 14: Рівноприскорений рух
    // ==========================================================
    double a, t, v; // a - прискорення, t - час, v - швидкість
    double S, T;    // S - відстань, T - шуканий час

    printf("\n=== TASK 14 RESULTS ===\n");

    printf("\n=== TASK 14 RESULTS ===\n");
    printf("Введіть прискорення: ");
    scanf("%lf", &a);
    printf("Введіть час руху: ");
    scanf("%lf", &t);
    printf("Введіть швидкість: ");
    scanf("%lf", &v);
   
    // 1. Відстань S = (a * t^2) / 2
    S = (a * t * t) / 2.0;

    // 2. Час досягнення швидкості T = v / a
    T = v / a;

    printf("\nРезультати:\n");
    printf("S = %.2f\n", S);
    printf("T = %.2f\n", T);

        return 0;
}