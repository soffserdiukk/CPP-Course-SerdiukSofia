#include <stdio.h>

int main() {
    // --- ЗАВДАННЯ 17: Піднесення до 8-го степеня ---
    double x17, res17;

    printf("=== РЕЗУЛЬТАТИ ЗАВДАННЯ 17 ===\n");
    printf("Введіть число (від 0 до 10000): ");
    scanf("%lf", &x17);

    res17 = x17 * x17;      // x^2
    res17 = res17 * res17;  // x^4
    res17 = res17 * res17;  // x^8

    // Форматування %25.4f для точного вирівнювання
    printf("Результат x^8:%25.4f\n", res17);

    // --- ЗАВДАННЯ 14: Рівноприскорений рух ---
    double a, t, v; 
    double S, T;

    printf("\n=== РЕЗУЛЬТАТИ ЗАВДАННЯ 14 ===\n");
    printf("Введіть прискорення (a): ");
    scanf("%lf", &a);
    printf("Введіть час руху (t): ");
    scanf("%lf", &t);
    printf("Введіть швидкість (v): ");
    scanf("%lf", &v);
   
    // 1. Відстань S = (a * t^2) / 2
    S = (a * t * t) / 2.0;

    // 2. Час досягнення швидкості T = v / a
    T = v / a;

    printf("\nРезультати:\n");
    printf("S = %.2f\n", S);
    printf("T = %.2f\n", T);

    return 0;
}