#include <stdio.h>
#include <math.h>

int main() {
    // ==========================================================
    // 1) Тригонометричний косинус
    // ==========================================================
    double x, res_cos;
    printf("--- Завдання 1 ---\n");
    printf("Введіть число x (в радіанах): ");
    scanf("%lf", &x);

    res_cos = cos(x);
    printf("cos(%g) = %.4f\n", x, res_cos);


    // ==========================================================
    // 2) Гіпотенуза прямокутного трикутника
    // ==========================================================
    double a, b, c;
    printf("\n--- Завдання 2 ---\n");
    printf("Введіть катети a та b: ");
    scanf("%lf %lf", &a, &b);

    // c = sqrt(a^2 + b^2)
    c = sqrt(a * a + b * b);
    printf("Гіпотенуза c = %.2f\n", c);

    // ==========================================================
    // 3) Площа трикутника за формулою Герона
    // ==========================================================
    double s1, s2, s3, p, S;
    printf("\n--- Завдання 3 ---\n");
    printf("Введіть сторони трикутника s1, s2, s3: ");
    scanf("%lf %lf %lf", &s1, &s2, &s3);

    // p - півпериметр
    p = (s1 + s2 + s3) / 2.0;
    
    // площа
    S = sqrt(p * (p - s1) * (p - s2) * (p - s3));

    printf("S = %.2f\n", S);

    // ==========================================================
    // 4) Многочлен: y = x^5 + x^3 + x
    // ==========================================================
    double x1, x2, y;
    printf("\n--- Завдання 4 (пункт е) ---\n");
    printf("Введіть x: ");
    scanf("%lf", &x1);

    // Оптимізація (схема Горнера): 
    // y = x * (x^4 + x^2 + 1) = x * (x^2 * (x^2 + 1) + 1)
    x2 = x1 * x1;               // 1-ша операція множення
    y = x1 * (x2 * (x2 + 1) + 1); // ще 2 операції множення

    printf("y = %.2f\n", y);


    // ==========================================================
    // 5) Функція Розенброка: 100*(x^2 - y)^2 + (x - 1)^2
    // ==========================================================
    double x_r, y_r, res_r;
    printf("\n--- Завдання 5 ---\n");
    printf("Введіть x та y: ");
    scanf("%lf %lf", &x_r, &y_r);

    // Обчислюємо за формулою: 100 * (x^2 - y)^2 + (x - 1)^2
    res_r = 100.0 * pow((x_r * x_r - y_r), 2) + pow((x_r - 1.0), 2);

    printf("Rosenbrock2d(%g, %g) = %.4f\n", x_r, y_r, res_r);

    // ==========================================================
    // 6) Площа трикутника за координатами вершин
    // ==========================================================
    double xA, yA, xB, yB, xC, yC;
    double sideAB, sideBC, sideAC, p_coord, S_coord;

    printf("\n--- Завдання 6 ---\n");

    // Введення за специфічним форматом з умови
    printf("Введіть координати точки А (x y): ");
    scanf("%lf %lf", &xA, &yA);

    printf("Введіть координати точки B: ");
    scanf("%lf %lf", &xB, &yB);

    printf("Введіть координати точки C: ");
    scanf("%lf %lf", &xC, &yC);

    // 1. Рахуємо довжини сторін
    sideAB = sqrt(pow(xB - xA, 2) + pow(yB - yA, 2));
    sideBC = sqrt(pow(xC - xB, 2) + pow(yC - yB, 2));
    sideAC = sqrt(pow(xC - xA, 2) + pow(yC - yA, 2));

    // 2. Рахуємо площу через півпериметр (Герон)
    p_coord = (sideAB + sideBC + sideAC) / 2.0;
    S_coord = sqrt(p_coord * (p_coord - sideAB) * (p_coord - sideBC) * (p_coord - sideAC));

    printf("\nДовжини сторін: AB=%.2f, BC=%.2f, AC=%.2f\n", sideAB, sideBC, sideAC);
    printf("Площа трикутника S = %.4f\n", S_coord);

    return 0;
        }